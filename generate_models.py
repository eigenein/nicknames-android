#!/usr/bin/env python3

"""
This script is supposed to generate the models source code with the Markov chains definitions.
"""

from collections import Counter, defaultdict
from contextlib import redirect_stdout
from datetime import datetime
from itertools import chain
from pathlib import Path
from sys import stderr
from typing import Dict, Iterable, List, Optional, Tuple


def main():
    for key, words in vocabulary:
        path = models_path / f'{key.replace("_", " ").title().replace(" ", "")}.kt'
        print(f"{path}:", file=stderr)

        with path.open("wt") as fp, redirect_stdout(fp):
            # Build the model. Remove possible duplicates in the vocabulary.
            model = make_model(set(words))

            # Header.
            print(f"/*")
            print(f" * Do not edit.")
            print(f" * Generated with `{Path(__file__).name}` on {datetime.now()}.")
            print(f" * {len(model)} bigram entries.")
            print(f" * {sum(len(endings) for endings in model.values())} ending entries.")
            print(f" */")
            print(f"")
            print(f"val {key} = mapOf(")

            # For each bigram prefix make an array.
            for i, ((first, second), endings) in enumerate(model.items(), 1):
                print(f"    Pair({make_literal(first)}, {make_literal(second)}) to arrayOf(")

                # For each trigram ending make an entry.
                for j, (third, p) in enumerate(endings, 1):
                    print(
                        f"        Pair({make_literal(third)}, {p}f){',' if j != len(endings) else ''}"
                    )

                # Close the prefix array.
                print(f"    ){',' if i != len(model) else ''}")

            # Footer.
            print(f")")


def make_trigrams(word: str) -> List[Tuple[Optional[str], Optional[str], Optional[str]]]:
    chars = (None, None, *word, None, None)
    return [chars[i : i + 3] for i in range(len(chars) - 2)]


def make_model(
    words: Iterable[str],
) -> Dict[Tuple[Optional[str], Optional[str]], Tuple[Optional[str], float]]:
    # Make a list of all the trigrams in the word list.
    trigrams = list(chain.from_iterable(make_trigrams(word) for word in words))

    # Count unique trigrams and their bigram prefixes.
    trigram_counter = Counter(trigrams)
    bigram_counter = Counter(tuple(bigram) for *bigram, _ in trigrams)
    print(f"{len(trigram_counter)} unique trigrams.", file=stderr)

    model = defaultdict(list)
    # I had to limit the number of unique trigrams because of the Java method size limitation.
    for (first, second, third), nr_trigram in trigram_counter.most_common(2500):
        # For each bigram prefix make a entry with the trigram ending and probability.
        model[first, second].append(
            (third, nr_trigram / bigram_counter[first, second])
        )

    return model


def make_literal(value: Optional[str]) -> str:
    """
    Make a Kotlin literal from the value.
    """
    assert value is None or len(value) == 1
    return f"'{value}'" if value else "Unit"


